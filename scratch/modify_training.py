        "        )\n",
        "        \n",
        "        best_val_loss = float('inf')\n",
        "        patience_counter = 0\n",
        "        fold_best_path = f\"{OUTPUT_DIR}/model/best_model_fold_{fold_idx}.bin\"\n",
        "        \n",
        "        fold_history = {\n",
