        "                \n",
        "        cv_history.append(fold_history)\n",
        "        \n",
        "    end_time = time.time()\n",
        "    print(f\"\\nCross-validation completed in {(end_time - start_time)/60:.2f} minutes.\")\n",
        "    return cv_history\n",
        "\n",
