        "            fold_history['train_acc'].append(train_acc.item())\n",
        "            fold_history['val_loss'].append(val_loss)\n",
        "            fold_history['val_acc'].append(val_acc.item())\n",
        "            \n",
        "            # Early stopping monitors val_loss\n",
        "            if val_loss < best_val_loss:\n",
