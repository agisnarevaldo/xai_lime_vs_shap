        "        \n",
        "        fold_train_loader = create_data_loader(fold_train_df, tokenizer, MAX_LEN, BATCH_SIZE)\n",
        "        fold_val_loader = create_data_loader(fold_val_df, tokenizer, MAX_LEN, BATCH_SIZE, shuffle=False)\n",
        "        \n",
        "        # Initialize model, optimizer, scheduler for this fold\n",
        "        fold_model = initialize_model(MODEL_NAME, NUM_LABELS, CLASSIFIER_DROPOUT)\n",
        "        fold_optimizer = AdamW(fold_model.parameters(), lr=learning_rate, weight_decay=WEIGHT_DECAY)\n",
        "        \n",
        "        total_steps = len(fold_train_loader) * EPOCHS\n",
        "        warmup_steps = max(1, int(total_steps * WARMUP_RATIO))\n",
