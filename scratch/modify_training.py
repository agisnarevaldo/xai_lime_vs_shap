        "            all_labels.extend(labels.detach().cpu().numpy().tolist())\n",
        "\n",
        "    metrics = compute_metrics(all_labels, all_preds)\n",
        "    return correct_predictions.double() / n_examples, np.mean(losses), metrics, all_labels, all_preds"
