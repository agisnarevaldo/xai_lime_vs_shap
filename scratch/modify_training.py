        "# Standard CrossEntropyLoss with Label Smoothing (no class weights)\n",
        "loss_fn = nn.CrossEntropyLoss(label_smoothing=LABEL_SMOOTHING).to(DEVICE)\n",
        "\n",
        "def compute_metrics(y_true, y_pred):\n",
